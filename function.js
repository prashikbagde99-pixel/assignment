// ==================function in js================================
function areaofcircle(r){
    result = 3.14*r**2
    return result
}
console.log("area of circle is",areaofcircle(5))

function perimeterofrectangle(l,w){
    result = 2*(l+w)
    return result
}
console.log("perimeter of rectangle is",perimeterofrectangle(4,5))

function volumeofcube(a){
    result = a**3
    return result
}
console.log("volume of cube", volumeofcube(4))

function simpleinterest(p,r,t){
    result = (p*r*t)/100
    return result
}
console.log("simple interest",simpleinterest(7300,5,2))

function averageofnumber(x,y,z){
    result = (x+y+z)/3
    return result
}
console.log("average of numbers", averageofnumber(48,23,56))

function celsiustofahrenheit(c){
    result = (c * 9/5) + 32
    return result
}
console.log("celsius to fahrenheit ", celsiustofahrenheit(20))

function kmtoMiles(km){
    result = km*0.621371
    return result
}
console.log("km to miles",kmtoMiles(9))

function square(num){
    result = num**2
    return result
}
console.log("square ",square(5))

function add(a,b){
    result = a+b
    return result
}
console.log("add ",add(6,7))

function multiplyThree(a,b,c){
result = a*b*c
return result
}
console.log("multiply ",multiplyThree(2,3,4))

function subtraction(a,b){
result = a-b
return result
}
console.log("subtract",subtraction(9,3))

